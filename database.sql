CREATE DATABASE IF NOT EXISTS `library_chatbot`
  CHARACTER SET utf8mb4
  COLLATE utf8mb4_unicode_ci;

USE `library_chatbot`;

-- 先删除旧表（有外键依赖，顺序不能乱）
SET FOREIGN_KEY_CHECKS = 0;
DROP TABLE IF EXISTS `feedbacks`;
DROP TABLE IF EXISTS `chat_histories`;
DROP TABLE IF EXISTS `faqs`;
DROP TABLE IF EXISTS `faq_categories`;
DROP TABLE IF EXISTS `books`;
DROP TABLE IF EXISTS `users`;
SET FOREIGN_KEY_CHECKS = 1;

-- ================================================================
-- 用户表
-- ================================================================
CREATE TABLE `users` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `username` VARCHAR(80) NOT NULL COMMENT '用户名',
  `email` VARCHAR(120) NOT NULL COMMENT '邮箱',
  `password_hash` VARCHAR(256) NOT NULL COMMENT '密码哈希',
  `nickname` VARCHAR(50) DEFAULT NULL COMMENT '昵称',
  `role` VARCHAR(20) DEFAULT 'user' COMMENT 'user/admin',
  `avatar` VARCHAR(200) DEFAULT NULL,
  `created_at` DATETIME DEFAULT CURRENT_TIMESTAMP,
  `last_login` DATETIME DEFAULT NULL,
  PRIMARY KEY (`id`),
  UNIQUE KEY (`username`),
  UNIQUE KEY (`email`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='用户表';

-- ================================================================
-- FAQ分类表
-- ================================================================
CREATE TABLE `faq_categories` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `name` VARCHAR(50) NOT NULL COMMENT '分类名称',
  `description` VARCHAR(200) DEFAULT NULL,
  `icon` VARCHAR(50) DEFAULT 'folder',
  `color` VARCHAR(20) DEFAULT '#4A90A4',
  `sort_order` INT DEFAULT 0,
  `is_active` TINYINT(1) DEFAULT 1,
  PRIMARY KEY (`id`),
  UNIQUE KEY (`name`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='FAQ分类表';

-- ================================================================
-- FAQ问答对表
-- ================================================================
CREATE TABLE `faqs` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `question` TEXT NOT NULL COMMENT '问题',
  `answer` TEXT NOT NULL COMMENT '答案',
  `category_id` INT DEFAULT NULL,
  `keywords` VARCHAR(500) DEFAULT NULL COMMENT '关键词逗号分隔',
  `similar_questions` TEXT DEFAULT NULL COMMENT '相似问法换行分隔',
  `hit_count` INT DEFAULT 0 COMMENT '命中次数',
  `is_active` TINYINT(1) DEFAULT 1,
  `created_at` DATETIME DEFAULT CURRENT_TIMESTAMP,
  `updated_at` DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  KEY (`category_id`),
  FOREIGN KEY (`category_id`) REFERENCES `faq_categories`(`id`) ON DELETE SET NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='FAQ问答对表';

-- ================================================================
-- 馆藏书目表
-- ================================================================
CREATE TABLE `books` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `title` VARCHAR(200) NOT NULL COMMENT '书名',
  `author` VARCHAR(150) DEFAULT NULL COMMENT '作者',
  `publisher` VARCHAR(100) DEFAULT NULL COMMENT '出版社',
  `isbn` VARCHAR(20) DEFAULT NULL,
  `publish_year` INT DEFAULT NULL,
  `category` VARCHAR(50) DEFAULT NULL COMMENT '学科分类',
  `call_number` VARCHAR(50) DEFAULT NULL COMMENT '索书号',
  `location` VARCHAR(100) DEFAULT NULL COMMENT '馆藏位置',
  `total_copies` INT DEFAULT 1,
  `available_copies` INT DEFAULT 1,
  `description` TEXT DEFAULT NULL,
  `cover_url` VARCHAR(300) DEFAULT NULL,
  PRIMARY KEY (`id`),
  UNIQUE KEY (`isbn`),
  KEY (`title`),
  KEY (`author`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='馆藏书目表';

-- ================================================================
-- 聊天记录表
-- ================================================================
CREATE TABLE `chat_histories` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `user_id` INT DEFAULT NULL,
  `session_id` VARCHAR(200) DEFAULT NULL COMMENT '会话ID',
  `user_message` TEXT NOT NULL,
  `bot_response` TEXT NOT NULL,
  `response_type` VARCHAR(20) DEFAULT 'faq',
  `matched_faq_id` INT DEFAULT NULL,
  `similarity_score` FLOAT DEFAULT NULL,
  `created_at` DATETIME DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  KEY (`user_id`),
  KEY (`created_at`),
  FOREIGN KEY (`user_id`) REFERENCES `users`(`id`) ON DELETE SET NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='聊天记录表';

-- ================================================================
-- 用户反馈表
-- ================================================================
CREATE TABLE `feedbacks` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `chat_id` INT DEFAULT NULL,
  `user_id` INT DEFAULT NULL,
  `rating` INT DEFAULT NULL COMMENT '1-5',
  `comment` TEXT DEFAULT NULL,
  `created_at` DATETIME DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  FOREIGN KEY (`chat_id`) REFERENCES `chat_histories`(`id`) ON DELETE CASCADE,
  FOREIGN KEY (`user_id`) REFERENCES `users`(`id`) ON DELETE SET NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='用户反馈表';


